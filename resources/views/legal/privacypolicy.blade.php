What is Personal Data

In the course of engaging for our HeyHo Services, we will request that you provide data and information about yourself and/or your organisation (“Personal Data”) to enable us to enter into transaction with you or to deliver the necessary services and/or deliverables in connection with our business.

 

Personal data means any data relates directly or indirectly to a data subject, who is identified or identifiable from that information or from that and other information in the possession of a data user, including any sensitive personal data and expression of opinion about the data subject; but does not include any information that is processed for the purpose of a credit reporting business carried on by a credit reporting agency under the Credit Reporting Agencies Act 2010.

Sensitive personal data means any personal data consisting of information as to the physical or mental health or condition of a data subject, his political opinions, his religious beliefs or other beliefs of a similar nature, the commission or alleged commission by him of any offence or any other personal data as the Minister may determine by order published in the Gazette. We will not process your Sensitive Personal Data, unless the you have given your “explicit consent” or where, for example, the processing is necessary for:

the purpose of any legal proceedings; or
(ii)   the purpose of obtaining legal advice; or

(iii)  establishing, exercising or defending the data user’s legal rights.

We do not intentionally gather Personal Data from visitors who are under the age of 13. If we learn that a child under the age of 13 has submitted Personal Data to HeyHo App, we will attempt to delete such data as soon as possible. If you believe that we might have any Personal Data from a child under 13, please contact us.

 

 

The following data is collected by or on behalf of HeyHo:

Data provided by users. This includes:
User Profile
We collect data when users create or update their HeyHo accounts. This may include their name, email, phone number, login name and password, address, profile picture, payment or banking information (including related payment verification information), driver’s license and other government identification documents (which may indicate document numbers as well as birth date, gender, and photo). This also includes vehicle or insurance information of drivers and delivery persons, emergency contact information, user settings, and evidence of health or fitness to provide services using HeyHo App. This also includes gender and/or occupation (when required for certain

HeyHo services or programs, if applicable). We may use the photos submitted by users to verify their identities, such as through facial recognition technologies.

Background check and identity verification: We collect background check and identity verification information for drivers and delivery persons. This may include information such as driver history or criminal record (where permitted by law), and right to work. This information may be collected by an authorized vendor on HeyHo’s behalf. We also collect identity verification from HeyHo users who request alcohol delivery.
Demographic data: We may collect demographic data about users, including through user surveys. In some countries, we may also receive demographic data about users from third parties.
User content: We collect the information users submit when they contact HeyHo customer support, provide ratings or compliments for other users or restaurant partners, or otherwise contact HeyHo. This may include feedback, photographs or other recordings collected by users.
Data created during use of our services. This includes: 
Location data (driver and delivery person): HeyHo collects this data when the HeyHo app is running in the foreground (app open and on-screen) or background (app open but not on-screen) of their mobile device.
Location data (riders and delivery recipients). We collect precise or approximate location data from riders’ and delivery recipients’ mobile devices if they enable us to do so. HeyHo collects this data from the time a ride or delivery is requested until it is finished, and any time the app is running in the foreground of their mobile device. We use this data to enhance your use of our apps, including to improve pick-up locations, enable safety features, and prevent and detect fraud. 
HeyHo collects driver location data, and links location data collected during a trip with their rider’s account, even if the rider has not enabled us to collect location data from their device. This enables us to offer services to the rider like receipt generation and customer support. 
Transaction information: We collect transaction information related to the use of our services, including the type of services requested or provided, order details, delivery information, date and time the service was provided, amount charged, distance travelled, and payment method. Additionally, if someone uses your promotion code, we may associate your name with that person.
Usage data: We collect data about how users interact with our services. This includes data such as access dates and times, app features or pages viewed, app crashes and other system activity, type of browser, and third-party sites or services used before interacting with our services. In some cases, we collect this data through cookies, pixels, tags, and similar tracking technologies that create and maintain unique identifiers.
 

Like many online services, we use cookies to collect information. “Cookies” are small pieces of information that a website sends to your computer’s hard drive while you are viewing the website. We may use both session Cookies (which expire once you close your web browser) and persistent Cookies (which stay on your computer until you delete them) to provide you with a more personal and interactive experience on our Site. Site areas accessible only to registered users require that your browser accept Cookies. HeyHo App uses Cookies to verify that you are properly signed in, to display information for your primary location, and to highlight your pending reservations, for example. We may also use Cookies from time to time to measure your response to new aspects of the HeyHo Services and/or emails in an effort to continually improve customer service and HeyHo Services usability. Please be aware that a Cookie cannot spread computer viruses, retrieve any other data from your hard drive, or capture your email address.

You can erase or block Cookies from your computer if you wish to do so (your internet browser help screen or manual will thoroughly explain this process), but certain parts of the HeyHo Services, as well as parts of the Site, may not work correctly or will not work at all if your browser is set not to accept Cookies.

 

Device data: We may collect data about the devices used to access our services, including the hardware models, device IP address, operating systems and versions, software, preferred languages, unique device identifiers, advertising identifiers, serial numbers, device motion data, and mobile network data.
 

Communications data: We enable users to communicate with each other and HeyHo through HeyHo’s mobile apps and websites. For example, we enable drivers and riders, and restaurants or delivery persons and delivery recipients, to call, text, or send other files to each other (generally without disclosing their telephone numbers to each other). To provide this service, HeyHo receives some data regarding the calls, texts, or other communications, including the date and time of the communications and the content of the communications. HeyHo may also use this data for customer support services (including to resolve disputes between users), for safety and security purposes, to improve our products and services, and for analytics.
 

