HeyHo account owners who request services for or on behalf of other users, or who enable such users to request or receive services through their accounts. This includes owners of HeyHo for Business accounts.
Users or others providing information in connection with claims or disputes.
HeyHo business partners through which users create or access their HeyHo account, such as payment providers, social media services, or apps or websites that use HeyHo’s APIs or whose APIs HeyHo uses. 
Vendors who help us verify users’ identity, background information, and eligibility to work, for regulatory, safety, and security purposes. 
Insurance, vehicle, or financial services providers for drivers and/or delivery persons.
